package Model;

public class Queen {

    private int x;
    private int y;
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
    public void setX(int x) {
        this.x = x;
    }
    public void setY(int y) {
        this.y = y;
    }
    public Queen(int x, int y) {
        super();
        this.x = x;
        this.y = y;
    }
}